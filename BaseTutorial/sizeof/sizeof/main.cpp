#include <iostream>

using namespace std;

int main()
{
    int i;
    string s = "aaaaaa";

    cout << "sizeof(i):" << sizeof(i) << endl;
    cout << "sizeof(int):" << sizeof(int) << endl;

    cout << "sizeof(s):" << sizeof(s) << endl;
    cout << "sizeof(string):" << sizeof(string) << endl;

    return 0;
}
